# WHILE LOOP - - - prefered when we dont know the no.of iterations
count = 1
while count<= 10:
    print(count)
    count+=1

# FOR LOOP - - -  prefered when we know the no.of iterations
for i in range(1,101):
    if i == 5:
        break  #breaks loop
    print(i) 

for i in range (1,20):
    if(i%2==0):
        continue #skips that particular iteration
    print(i)

